'use strict';

/**
 * Calculates the sum of two numbers.
 *
 * @param {number} number1 - The first number.
 * @param {number} number2 - The second number.
 * @returns {number} The sum of the two numbers.
*/
function sum(number1, number2) {
  return number1 + number2;
}

module.exports = { sum };